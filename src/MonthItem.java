public class MonthItem {
    String itemName;
    Boolean isExpense;
    Integer quantity;
    Integer sumOfOne;

    MonthItem(String itemName, Boolean isExpense, Integer quantity, Integer sumOfOne) {
        this.itemName = itemName;
        this.isExpense = isExpense;
        this.quantity = quantity;
        this.sumOfOne = sumOfOne;

    }
}

